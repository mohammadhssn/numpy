"""
    https://numpy.org/doc/1.26/reference/random/index.html
"""

import numpy as np

rng = np.random.default_rng()

print(rng.integers(0, 100, size=(2, 3), endpoint=True))
"""
[[41 55  3]
[38 17 100]
"""
# ----------------------------------------------------------------
rng_seed = np.random.default_rng(seed=1234)
print(rng_seed.integers(0, 100))  # always return Repeated number
# ----------------------------------------------------------------
print(rng.random(size=(2, 3, 4)))
"""
[[[0.36553688 0.6997868  0.52768444 0.43438994]
  [0.11048447 0.79320512 0.92319552 0.42552694]
  [0.95763425 0.11996835 0.73957034 0.05644466]]

 [[0.10920866 0.52694279 0.5035025  0.69560001]
  [0.91944929 0.68325913 0.55470218 0.40303449]
  [0.00850494 0.21641793 0.6340713  0.03192098]]]
"""
# ----------------------------------------------------------------
print(rng.choice(a=[1, 2, 3, 4, 5, 6], size=(2, 3), replace=True, p=[0.1, 0.2, 0.7, 0.0, 0.0, 0.0]))
"""
[[3 1 3]
 [3 3 3]]
"""
# ----------------------------------------------------------------
print(rng.bytes(3))  # b'(\xdb\xf9'
# ----------------------------------------------------------------
a = np.array([[1, 2, 3], [4, 5, 6]])
rng.shuffle(a, axis=1)
print(a)
"""
[[1 3 2]
 [4 6 5]]
"""
# ----------------------------------------------------------------
print(rng.permutation(a))  # create new array
"""
[[2 1 3]
 [5 4 6]]
"""
